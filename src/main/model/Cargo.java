package main.model;

public class Cargo {

	String cargo_cod;
	String cargo_descricao;
	String cargo_nivel;
	
	
	public String getCargo_cod() {
		return cargo_cod;
	}
	public void setCargo_cod(String cargo_cod) {
		this.cargo_cod = cargo_cod;
	}
	public String getCargo_descricao() {
		return cargo_descricao;
	}
	public void setCargo_descricao(String cargo_descricao) {
		this.cargo_descricao = cargo_descricao;
	}
	public String getCargo_nivel() {
		return cargo_nivel;
	}
	public void setCargo_nivel(String cargo_nivel) {
		this.cargo_nivel = cargo_nivel;
	}
	
	
}
