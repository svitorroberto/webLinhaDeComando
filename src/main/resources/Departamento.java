package main.resources;

public class Departamento {

	String depto_cod;
	String depto_descricao;
	String depto_ramal;
	
	public String getDepto_cod() {
		return depto_cod;
	}
	public void setDepto_cod(String depto_cod) {
		this.depto_cod = depto_cod;
	}
	public String getDepto_descricao() {
		return depto_descricao;
	}
	public void setDepto_descricao(String depto_descricao) {
		this.depto_descricao = depto_descricao;
	}
	public String getDepto_ramal() {
		return depto_ramal;
	}
	public void setDepto_ramal(String depto_ramal) {
		this.depto_ramal = depto_ramal;
	}
	
	
	
	
}
